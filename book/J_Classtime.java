import java.lang.Math.*;

public class J_Classtime{
    public static void main(String args[]){
        int n1 = 5;
        int n4 = n1 << 29;
        int n5 = n4 >> 29;
        int n6 = n4 >>> 29;
        System.out.println("n4: "+n4);
        System.out.println("n5: "+n5);
        System.out.println("n6: "+n6);
    }
}